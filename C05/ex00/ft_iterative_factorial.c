int	ft_iterative_factorial(int nb)
{
	int	n;

	if (nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	n = 1;
	while (nb)
	{
		n *= nb;
		nb--;
	}
	return (n);
}
