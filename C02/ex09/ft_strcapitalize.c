char	*ft_strcapitalize(char *str)
{
	int	n;

	if (str[0] >= 'a' && str[0] <= 'z')
		str[0] -= 32;
	n = 1;
	while (str[n] != 0)
	{
		if (!((str[n - 1] >= 'a' && str[n - 1] <= 'z')
				|| (str[n - 1] >= 'A' && str[n - 1] <= 'Z')
				|| (str[n - 1] >= '0' && str[n - 1] <= '9')))
		{
			if (str[n] >= 'a' && str[n] <= 'z')
				str[n] -= 32;
		}
		else
		{
			if (str[n] >= 'A' && str[n] <= 'Z')
				str[n] += 32;
		}
		n++;
	}
	return (str);
}
